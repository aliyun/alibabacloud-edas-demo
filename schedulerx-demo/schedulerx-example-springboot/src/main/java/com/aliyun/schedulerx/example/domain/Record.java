package com.aliyun.schedulerx.example.domain;

/**
 *
 * @author xiaomeng.hxm
 */
public class Record {
    private int id;
    private String name;
    private String data;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

}
